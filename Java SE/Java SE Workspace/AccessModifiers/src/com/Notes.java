package com;

public class Notes {
	
	/*
	 * 	Access Modifiers
	 * ---------------------
	 * 	1. default
	 * 	2. public
	 * 	3. private
	 * 	4. protected
	 * 
	 * 1. default
	 * ---------------
	 * -> Data is visible to the classes with in the same package.
	 * 
	 * 2. public
	 * ---------
	 * -> data is visible to all the classes from the same package, and to other packages too
	 * 
	 * 4. protected
	 * --------------
	 * -> Data is visible to all the classes from the same packages and 
	 *    sub classes of other packages
	 *  
	 * 3. Private
	 * ------------
	 * -> data is visible with in the same class. 
	 * 
	 * 
	 */

}
